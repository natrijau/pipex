#include "libft.h"

long	ft_atoi(const char *nptr)
{
	unsigned int	i;
	unsigned int	sign;
	long			nb;

	i = 0;
	nb = 0;
	sign = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign = sign + 1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		nb = (nb * 10) + (nptr[i] - 48);
		i++;
	}
	if (sign == 1)
		nb = nb * -1;
	return (nb);
}
